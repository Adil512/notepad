const baseMeta = writingToolsMeta["character-counter"];

export function characterCounterHeadlineFromTitle(
  title: string,
  fallback: string
): string {
  for (const sep of [" – ", " - "] as const) {
    const i = title.indexOf(sep);
    if (i > 0) return title.slice(0, i).trim();
  }
  return title.length <= 96 ? title.trim() : fallback;
}

export function getCharacterCounterSeo(locale: string): {
  title: string;
  description: string;
} {
  const pack = characterCounterSeo[locale];
  if (pack) return pack;
  return { title: baseMeta.title, description: baseMeta.description };
}

export function getCharacterCounterHero(locale: string): {
  h1: string;
  description: string;
} {
  const { title, description } = getCharacterCounterSeo(locale);
  return {
    h1: characterCounterHeadlineFromTitle(title, baseMeta.h1),
    description,
  };
}

const baseMeta = writingToolsMeta["json-formatter"];

export function jsonFormatterHeadlineFromTitle(title: string, fallback: string): string {
  for (const sep of [" – ", " - "] as const) {
    const i = title.indexOf(sep);
    if (i > 0) return title.slice(0, i).trim();
  }
  return title.length <= 96 ? title.trim() : fallback;
}

export function getJsonFormatterSeo(locale: string): { title: string; description: string } {
  const pack = jsonFormatterSeo[locale];
  if (pack) return pack;
  return { title: baseMeta.title, description: baseMeta.description };
}

export function getJsonFormatterHero(locale: string): { h1: string; description: string } {
  const { title, description } = getJsonFormatterSeo(locale);
  return {
    h1: jsonFormatterHeadlineFromTitle(title, baseMeta.h1),
    description,
  };
}

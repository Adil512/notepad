const baseMeta = writingToolsMeta["duplicate-remover"];

export function duplicateRemoverHeadlineFromTitle(
  title: string,
  fallback: string
): string {
  for (const sep of [" – ", " - "] as const) {
    const i = title.indexOf(sep);
    if (i > 0) return title.slice(0, i).trim();
  }
  return title.length <= 96 ? title.trim() : fallback;
}

export function getDuplicateRemoverSeo(locale: string): {
  title: string;
  description: string;
} {
  const pack = duplicateRemoverSeo[locale];
  if (pack) return pack;
  return { title: baseMeta.title, description: baseMeta.description };
}

export function getDuplicateRemoverHero(locale: string): {
  h1: string;
  description: string;
} {
  const { title, description } = getDuplicateRemoverSeo(locale);
  return {
    h1: duplicateRemoverHeadlineFromTitle(title, baseMeta.h1),
    description,
  };
}

const baseMeta = writingToolsMeta["text-cleaner"];

export function textCleanerHeadlineFromTitle(
  title: string,
  fallback: string
): string {
  for (const sep of [" – ", " - "] as const) {
    const i = title.indexOf(sep);
    if (i > 0) return title.slice(0, i).trim();
  }
  return title.length <= 96 ? title.trim() : fallback;
}

export function getTextCleanerSeo(locale: string): {
  title: string;
  description: string;
} {
  const pack = textCleanerSeo[locale];
  if (pack) return pack;
  return { title: baseMeta.title, description: baseMeta.description };
}

export function getTextCleanerHero(locale: string): {
  h1: string;
  description: string;
} {
  const { title, description } = getTextCleanerSeo(locale);
  return {
    h1: textCleanerHeadlineFromTitle(title, baseMeta.h1),
    description,
  };
}

const baseMeta = writingToolsMeta["text-sorter"];

export function textSorterHeadlineFromTitle(
  title: string,
  fallback: string
): string {
  for (const sep of [" – ", " - "] as const) {
    const i = title.indexOf(sep);
    if (i > 0) return title.slice(0, i).trim();
  }
  return title.length <= 96 ? title.trim() : fallback;
}

export function getTextSorterSeo(locale: string): {
  title: string;
  description: string;
} {
  const pack = textSorterSeo[locale];
  if (pack) return pack;
  return { title: baseMeta.title, description: baseMeta.description };
}

export function getTextSorterHero(locale: string): {
  h1: string;
  description: string;
} {
  const { title, description } = getTextSorterSeo(locale);
  return {
    h1: textSorterHeadlineFromTitle(title, baseMeta.h1),
    description,
  };
}

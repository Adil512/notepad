const baseMeta = writingToolsMeta["html-editor"];

export function htmlEditorHeadlineFromTitle(
  title: string,
  fallback: string
): string {
  for (const sep of [" – ", " - "] as const) {
    const i = title.indexOf(sep);
    if (i > 0) return title.slice(0, i).trim();
  }
  return title.length <= 96 ? title.trim() : fallback;
}

export function getHtmlEditorSeo(locale: string): {
  title: string;
  description: string;
} {
  const pack = htmlEditorSeo[locale];
  if (pack) return pack;
  return { title: baseMeta.title, description: baseMeta.description };
}

export function getHtmlEditorHero(locale: string): {
  h1: string;
  description: string;
} {
  const { title, description } = getHtmlEditorSeo(locale);
  return {
    h1: htmlEditorHeadlineFromTitle(title, baseMeta.h1),
    description,
  };
}

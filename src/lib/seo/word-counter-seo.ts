const baseMeta = writingToolsMeta["word-counter"];

export function wordCounterHeadlineFromTitle(
  title: string,
  fallback: string
): string {
  for (const sep of [" – ", " - "] as const) {
    const i = title.indexOf(sep);
    if (i > 0) return title.slice(0, i).trim();
  }
  return title.length <= 96 ? title.trim() : fallback;
}

export function getWordCounterSeo(locale: string): {
  title: string;
  description: string;
} {
  const pack = wordCounterSeo[locale];
  if (pack) return pack;
  return { title: baseMeta.title, description: baseMeta.description };
}

export function getWordCounterHero(locale: string): {
  h1: string;
  description: string;
} {
  const { title, description } = getWordCounterSeo(locale);
  return {
    h1: wordCounterHeadlineFromTitle(title, baseMeta.h1),
    description,
  };
}

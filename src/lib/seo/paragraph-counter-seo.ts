const baseMeta = writingToolsMeta["paragraph-counter"];

export function paragraphCounterHeadlineFromTitle(
  title: string,
  fallback: string
): string {
  for (const sep of [" – ", " - "] as const) {
    const i = title.indexOf(sep);
    if (i > 0) return title.slice(0, i).trim();
  }
  return title.length <= 96 ? title.trim() : fallback;
}

export function getParagraphCounterSeo(locale: string): {
  title: string;
  description: string;
} {
  const pack = paragraphCounterSeo[locale];
  if (pack) return pack;
  return { title: baseMeta.title, description: baseMeta.description };
}

export function getParagraphCounterHero(locale: string): {
  h1: string;
  description: string;
} {
  const { title, description } = getParagraphCounterSeo(locale);
  return {
    h1: paragraphCounterHeadlineFromTitle(title, baseMeta.h1),
    description,
  };
}

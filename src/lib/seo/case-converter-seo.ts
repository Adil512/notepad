const baseMeta = writingToolsMeta["case-converter"];

export function caseConverterHeadlineFromTitle(
  title: string,
  fallback: string
): string {
  for (const sep of [" – ", " - "] as const) {
    const i = title.indexOf(sep);
    if (i > 0) return title.slice(0, i).trim();
  }
  return title.length <= 96 ? title.trim() : fallback;
}

export function getCaseConverterSeo(locale: string): {
  title: string;
  description: string;
} {
  const pack = caseConverterSeo[locale];
  if (pack) return pack;
  return { title: baseMeta.title, description: baseMeta.description };
}

export function getCaseConverterHero(locale: string): {
  h1: string;
  description: string;
} {
  const { title, description } = getCaseConverterSeo(locale);
  return {
    h1: caseConverterHeadlineFromTitle(title, baseMeta.h1),
    description,
  };
}

const baseMeta = writingToolsMeta["text-compare-diff"];

export function textCompareDiffHeadlineFromTitle(
  title: string,
  fallback: string
): string {
  for (const sep of [" – ", " - "] as const) {
    const i = title.indexOf(sep);
    if (i > 0) return title.slice(0, i).trim();
  }
  return title.length <= 96 ? title.trim() : fallback;
}

export function getTextCompareDiffSeo(locale: string): {
  title: string;
  description: string;
} {
  const pack = textCompareDiffSeo[locale];
  if (pack) return pack;
  return { title: baseMeta.title, description: baseMeta.description };
}

export function getTextCompareDiffHero(locale: string): {
  h1: string;
  description: string;
} {
  const { title, description } = getTextCompareDiffSeo(locale);
  return {
    h1: textCompareDiffHeadlineFromTitle(title, baseMeta.h1),
    description,
  };
}
